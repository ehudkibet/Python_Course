# Python while loop
# increment
'''i=0
while i<=10:
    print(i)
    i+=1'''

# Break statement- break loop even if the while condition is true
'''i=0
while i<7:
    print(i)
    if i==3:
     break
    i+=1'''

# Continue statement - continue to next iteration
i = 0
while i < 8:
    i += 1
    if i == 3:
        continue
    print(i)


