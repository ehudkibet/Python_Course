#In this script, we'll create a python module to do basic calculations

#This function adds 2 numbers
def add(x,y):
    return x+y
#This function subtracts 2 numbers
def subtract(x,y):
    return x-y

#This function multiplies 2 numbers
def multiply(x,y):
    return x*y

#This function divides 2 numbers
def divide(x,y):
    return x/y
